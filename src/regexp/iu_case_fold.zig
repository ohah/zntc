/// cp 의 simple case-fold 등가 codepoint 들 (ASCII fast-path 포함).
/// out 에 append. 자기 자신은 호출부가 이미 보유하므로 제외.
pub fn appendEquivalents(cp: u32, out: *std.ArrayList(u32), a: std.mem.Allocator) !void {
    // ASCII swap 은 추가(early-return 아님) — k/K/s/S 는 테이블에도 존재
    // (Kelvin U+212A, ſ U+017F). regexpu getCaseEquivalents 와 동형.
    if (cp >= 'A' and cp <= 'Z') {
        try out.append(a, cp + 0x20);
    } else if (cp >= 'a' and cp <= 'z') {
        try out.append(a, cp - 0x20);
    }
    var lo: usize = 0;
    var hi: usize = TABLE.len;
    while (lo < hi) {
        const mid = lo + (hi - lo) / 2;
        if (TABLE[mid].cp == cp) {
            try out.appendSlice(a, TABLE[mid].eq);
            return;
        } else if (TABLE[mid].cp < cp) lo = mid + 1 else hi = mid;
    }
}
